class LocationAddress {
  final String name;
  final String street;
  final String ward;
  final String district;
  final String city;
  final String latitude;
  final String longitude;

  LocationAddress({
    required this.name,
    required this.street,
    required this.ward,
    required this.district,
    required this.city,
    required this.latitude,
    required this.longitude,
  });
}
